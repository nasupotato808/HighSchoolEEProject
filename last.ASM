	CJNE	R1,#00010000B,UP
	MOV	R1,#00000000B
	JMP	UP
DOWN:	JNB	P2.0,SPEED2
	JNB	P2.1,SPEED3
	JNB	P2.2,UP
	CLR	C
	MOV	P1,R4
	MOV	P3,R1
	CALL	DELAY
	XCH	A,R4
	SUBB	A,#01
	DA	A
	XCH	A,R4
	CJNE	R4,#01100101B,DOWN
	MOV	R4,#00001001B
