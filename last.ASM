	ORG	00H
	MOV	R4,#00H
	MOV	R1,#00H
START:	MOV	R2,#50
	MOV	R3,#10
UP:	JNB	P2.0,SPEED
	JNB	P2.1,SPEED1
	JNB	P2.2,DOWN
	MOV	P1,R4
	MOV	P3,R1
	CALL	DELAY	
	XCH	A,R4
