	SUBB	A,#06
	XCH	A,R2
	XCH	A,R3
	SUBB	A,#06
	XCH	A,R3
	CALL	DELAY
	SJMP	DOWN
DELAY:	XCH	A,R2
	MOV	R5,A
	XCH	A,R2
D1:	XCH	A,R3
	MOV	R6,A
	XCH	A,R3
D2:	MOV	R7,#248
	DJNZ	R7,$
	DJNZ	R6,D2
	DJNZ	R5,D1
